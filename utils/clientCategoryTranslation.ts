// Translate a category from English to German
export function translateCategory(
  englishCategory: string | undefined
): string | undefined {
  if (!englishCategory) {
    return undefined;
  }

  // Try exact match first
  let translation = categoryTranslations.get(englishCategory);
  if (translation) {
    return translation;
  }

  // Try lowercase match
  translation = categoryTranslations.get(englishCategory.toLowerCase());
  if (translation) {
    return translation;
  }

  // If no translation found, return original
  return englishCategory;
}

// Get all available translations (for debugging)
export function getAllTranslations(): Map<string, string> {
  return categoryTranslations;
}
